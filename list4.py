'''Differentiate between append () and extend () methods? 



append() is used to add a single element to the end of a list. The element can be of any data type,
including another list. When you append a list to another list using the append() method, the entire list 
becomes a single element of the original list. 


extend(), on the other hand, is used to append the elements of an iterable (e.g. another list or a tuple) 
to the end of a list. When you extend a list with another list using the extend() method, each element of 
the second list is added individually to the original list. '''